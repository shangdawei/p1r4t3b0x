package com.piratebox.utils;

/**
 * This class holds constants that are used to store preferences.
 * @author Aylatan
 */
public class PreferencesKeys {
    /**
     * The key used to store the root directory preference.
     */
    public static final String SELECT_DIR = "select_dir";
    /**
     * The key used to store the low battery check switch preference.
     */
    public static final String LOW_BAT = "low_bat";
    /**
     * The key used by the reset statistics button.
     */
    public static final String RESET_STAT = "reset_stat";
    /**
     * The key used by the help button.
     */
    public static final String HELP = "help";
    /**
     * The key used by the donate button.
     */
    public static final String BEER = "beer";
    /**
     * The key used to store the notification preference.
     */
    public static final String NOTIFICATION = "notification";
    /**
     * The key used to store the notification frequency preference.
     */
    public static final String NOTIFICATION_FREQUENCY = "notification_frequency";
    /**
     * The value of the default notification frequency.
     */
    public static final String NOTIFICATION_DEFAULT_FREQUENCY = "600";
    /**
     * The key used to store the notification vibrate preference.
     */
    public static final String NOTIFICATION_VIBRATE = "notification_vibrate";
    /**
     * The key used to store the notification ringtone preference.
     */
    public static final String NOTIFICATION_RINGTONE = "notification_ringtone";
}
